/**
 * Grid de icones para selecao
 * Exibe uma matriz de icones clicaveis similar a um emoji picker
 */
export function IconGridInput({
  value,
  onChange,
  label,
  description,
}: IconGridInputProps) {
  return (
    <div className="space-y-1.5">
      <label className="block text-xs font-medium text-gray-800 dark:text-gray-100">
        {label}
        {description && (
          <span className="text-gray-500 dark:text-gray-400 text-xs font-normal ml-1">
            {description}
          </span>
        )}
      </label>
      <div className="grid grid-cols-6 gap-1 p-2 bg-gray-50 dark:bg-gray-800 rounded-lg border border-gray-200 dark:border-gray-700">
        {HOVER_ICONS.map((icon) => {
          const isSelected = value === icon.name;
          return (
            <button
              key={icon.name}
              type="button"
              onClick={() => onChange(icon.name)}
              className={cn(
                "w-8 h-8 flex items-center justify-center rounded-md transition-all",
                "focus:outline-none focus:ring-2 focus:ring-blue-500",
                isSelected
                  ? "bg-blue-500 text-white ring-2 ring-blue-500 ring-offset-1"
                  : "bg-white dark:bg-gray-700 text-gray-600 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-600 border border-gray-200 dark:border-gray-600"
              )}
              title={icon.name}
            >
              <span className="w-4 h-4">{icon.svg}</span>
            </button>
          );
        })}
      </div>
    </div>
  );
}
